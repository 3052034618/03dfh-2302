function buildPricing(basePrice: number, floorPrice: number): PricingEntry[] {
  const entries: PricingEntry[] = [];
  const cityTiers: Array<['tier1' | 'tier2' | 'tier3', number]> = [
    ['tier1', 1.15],
    ['tier2', 1.0],
    ['tier3', 0.88],
  ];
  const storeLevels: Array<['flagship' | 'standard' | 'community', number]> = [
    ['flagship', 1.1],
    ['standard', 1.0],
    ['community', 0.92],
  ];
  const doctorLevels: Array<['director' | 'senior' | 'attending' | 'junior', number]> = [
    ['director', 1.25],
    ['senior', 1.1],
    ['attending', 1.0],
    ['junior', 0.9],
  ];

  for (const [ct, ctMult] of cityTiers) {
    for (const [sl, slMult] of storeLevels) {
      for (const [dl, dlMult] of doctorLevels) {
        const raw = basePrice * ctMult * slMult * dlMult;
        const price = Math.max(Math.round(raw / 100) * 100, floorPrice);
        entries.push({ cityTier: ct, storeLevel: sl, doctorLevel: dl, price });
      }
    }
  }
  return entries;
}

